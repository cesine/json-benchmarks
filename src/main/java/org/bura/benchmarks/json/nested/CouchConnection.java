package org.bura.benchmarks.json.nested;

public class CouchConnection {
    private String port;
    private String userFriendlyServerName;
    private String protocol;
    private String corpusid;
    private String authUrl;
    private String path;
    private String domain;
    private String pouchname;

    public String getPort() {
        return port;
    }

    public void setPort(String port) {
        this.port = port;
    }

    public String getUserFriendlyServerName() {
        return userFriendlyServerName;
    }

    public void setUserFriendlyServerName(String userFriendlyServerName) {
        this.userFriendlyServerName = userFriendlyServerName;
    }

    public String getProtocol() {
        return protocol;
    }

    public void setProtocol(String protocol) {
        this.protocol = protocol;
    }

    public String getCorpusid() {
        return corpusid;
    }

    public void setCorpusid(String corpusid) {
        this.corpusid = corpusid;
    }

    public String getAuthUrl() {
        return authUrl;
    }

    public void setAuthUrl(String authUrl) {
        this.authUrl = authUrl;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getDomain() {
        return domain;
    }

    public void setDomain(String domain) {
        this.domain = domain;
    }

    public String getPouchname() {
        return pouchname;
    }

    public void setPouchname(String pouchname) {
        this.pouchname = pouchname;
    }
}
      
      
